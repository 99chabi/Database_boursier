CREATE TABLE Beneficiaire(
    Nom VARCHAR(20),
    Prénom VARCHAR(20),
    Date_de_naissance DATE,
    Statut_matrimonial CHAR(7),
    Coordonnées VARCHAR(20),
    Formation VARCHAR(20),
    Bourse VARCHAR(20),
    Expérience VARCHAR(20),
    Metier VARCHAR(20),
    Appartenance_politique VARCHAR(20),
    Santé VARCHAR(20),
);
